//! 入玉宣言勝ちのルール設定
//!
//! 入玉宣言勝ちとは、自玉が相手陣（敵陣 3 段目以内）に入った状態で
//! 一定の条件を満たすとき、勝ちを宣言できるルールである。
//!
//! # 点数計算
//!
//! 宣言側の持ち駒と、敵陣にある宣言側の駒（玉を除く）を点数化する。
//!
//! - 大駒（飛車、角、馬、龍）: 5 点
//! - 小駒（それ以外）: 1 点
//! - 玉は計算に含めない
//!
//! # ルールの種類
//!
//! | ルール | 先手の宣言条件 | 後手の宣言条件 |
//! |--------|---------------|---------------|
//! | `Point24` | 31 点以上 + 敵陣に自駒 10 枚以上 | 同左 |
//! | `Point27` | 28 点以上 + 敵陣に自駒 10 枚以上 | 27 点以上 + 同左 |
//! | `TryRule` | 自玉が相手の玉の初期位置に到達 | 同左 |
//!
//! `Handicap` 付きのバリアントは駒落ち対局用で、欠けている駒点ぶん後手側の必要点が調整される。
//! いずれのルールでも、宣言時に自玉が王手されていないことが条件である。

/// 入玉ルール
#[derive(Copy, Clone, Debug, PartialEq, Eq, Default)]
pub enum EnteringKingRule {
    /// 入玉ルールなし
    None,
    /// 24点法（31点以上で宣言勝ち）
    Point24,
    /// 24点法（駒落ち対応）
    Point24Handicap,
    /// 27点法（CSAルール）
    Point27,
    /// 27点法（駒落ち対応）
    Point27Handicap,
    /// トライルール
    TryRule,
    /// 未設定
    #[default]
    Unset,
}
